package com.tuxedofish.challenges.binarytree;

public class PrintNode {
	int x, y, val;
	boolean hasLeft, hasRight;
	
	public PrintNode(int x, int y, int val, boolean hasLeft, boolean hasRight) {
		super();
		this.x = x;
		this.y = y;
		this.val = val;
		this.hasLeft = hasLeft;
		this.hasRight = hasRight;
	}
}
